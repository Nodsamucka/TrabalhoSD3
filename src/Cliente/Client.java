    public boolean iniciarConexao(String ip) {
        Conexao c = new Conexao();
        c.setIp(ip);
        c.setPorta("3322");

        try {
            clientes = new Socket(ip, 3322);
            return true;
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Não Foi Possivel Conectar", "Erro", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
            return false;
        }
    }

