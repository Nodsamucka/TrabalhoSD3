    private void enviaMensagem(String msg) {
        try {
            ObjectOutputStream output = new ObjectOutputStream(clientes.getOutputStream());
            ObjectInputStream input = new ObjectInputStream(clientes.getInputStream());
            output.writeUTF(msg);
            output.flush();
            System.out.println("A mensagem: "+msg+"' foi enviada.");
            String msgm = input.readUTF();
            System.out.println("Mensagem recebida: "+ msgm);
            output.writeUTF(ack);
            output.flush();
            
            if (!(msgm.equals("IAA"))) {
            System.out.println(input.readUTF());
            System.out.println(input.readUTF());
            }
            
        } catch (Exception e) {
            System.out.println("Falha ao enviar mensagem para o servidor!!");
        }

    }

